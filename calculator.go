package calculator

func Add(A, B float32) (Sum float32) {
	Sum = A + B
	return
}

func Subtract(A, B float32) (Sub float32) {
	Sub = A - B
	return
}

func Multiply(A, B float32) (Mul float32) {
	Mul = A * B
	return
}

func Divide(A, B float32) (Div float32) {
	Div = A / B
	return
}
